def test_mask_card():
    card_number = "Visa Platinum 1234567890123456"
    assert mask_card(card_number) == "Visa Platinum 1234 56** **** 3456"
    card_number = "Счет 1234567890123456"
    assert mask_card(card_number) == "Счет 1234 56** **** 3456"
    card_number = None
    assert mask_card(card_number) is None


def test_mask_account():
    account_number = "Visa Platinum 1234567890123456"
    assert mask_account(account_number) == "Visa Platinum **3456"

    account_number = None
    assert mask_account(account_number) is None
